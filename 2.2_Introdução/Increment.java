//Imprimindo multiplas linhas de texto com uma unica instrução


public class Increment {
    //o metodo main inicia a execução do aplicativo Java
    public static void main(String[] args) {
       
        int c;

        c=5;
        System.out.println(c);      //imprime5
        System.out.println(c++);    //imprime 5 pós incrementa
        System.out.println(c);      //imprime 6
        System.out.println();       // pula uma linha

        c=5;
        System.out.println(c);      //imprime5
        System.out.println(c++);    //imprime 5 pós incrementa
        System.out.println(c);      //imprime 6
              
    } //fim do main
    
}// fim da classe
